    private void Search(){
         try {
           con = Connect.connect();
            Statement s = con.createStatement();
            String Search = txtSearch.getText();
            String sql = "SELECT * FROM `embedded` where `IDEm` Like '%"+Search+"%' or `NameEm` Like '%"+Search+"%' or `FunctionEm` Like '%"+Search+"%'or `QuantityEm` = '"+Search+"' or `Type` Like '%"+Search+"%'";
            ResultSet rs = s.executeQuery(sql);
            while(rs.next()){ 
                String Quan = String.valueOf(rs.getInt(5));
                Object rows[] = new Object[6];
                rows[0] = rs.getString(1);
                rows[1] = rs.getString(2);  
                rows[2] = rs.getString(3);
                rows[3] = rs.getString(4);
                rows[4] = Quan;
                rows[5] = rs.getString(6);
                tableModel.addRow(rows);
            }
            con.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
     }
    
    private void Clear(){
        int dem = tblDevices.getRowCount();
        try{
        while (dem > 0 ){
            tableModel.removeRow(0);
        }
        }catch(java.lang.ArrayIndexOutOfBoundsException e){
            e.getMessage();
        }
    }
    
