    private void loadTable(){
         try {
            con = Connect.connect();
            
            Statement s = con.createStatement();
            
            ResultSet rs = s.executeQuery("SELECT * FROM embedded where StatusEm = 'Good' or StatusEm = 'Normal'");
            String []colsName = {"ID", "Name","Function","Status","Quantity","Type"};
            tableModel.setColumnIdentifiers(colsName); 
            tblDevices.setModel(tableModel);
            while(rs.next()){ 
                String Quan = String.valueOf(rs.getInt(5));
                Object rows[] = new Object[6];
                rows[0] = rs.getString(1);
                rows[1] = rs.getString(2);  
                rows[2] = rs.getString(3);
                rows[3] = rs.getString(4);
                rows[4] = Quan;
                rows[5] = rs.getString(6);
                tableModel.addRow(rows);
            }
            con.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    

