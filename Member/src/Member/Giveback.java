    private void Search(){
         try {
           con = Connect.connect();
            Statement s = con.createStatement();
            String Search = txtSearch.getText(); 
            String sql = "SELECT * FROM `giveback` where `FullName` = '"+name+"' and  (`IDEm` Like '%"+Search+"%' or `NameEm` Like '%"+Search+"%' or `Project` Like '%"+Search+"%'or `QuantityEm` = '"+Search+"' or `Type` Like '%"+Search+"%' or `Status` Like '%"+Search+"%')";
            ResultSet rs = s.executeQuery(sql);
            while(rs.next()){ 
                String Quan = String.valueOf(rs.getInt(6));
                Object rows[] = new Object[6];
                rows[0] = rs.getString(2);
                rows[1] = rs.getString(3);  
                rows[2] = rs.getString(4);
                rows[3] = rs.getString(5);
                rows[4] = Quan;
                rows[5] = rs.getString(7);
                tableModel.addRow(rows);
            }
            con.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
     }
    
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(txtSearch.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Search not empty!");
        }else{
            Clear();
            Search();
            int Count = tblGiveback.getRowCount();
            if(Count == 0){
                JOptionPane.showMessageDialog(null, "Don't search this database!");
                Clear();
            }else{
                Clear();
                Search();
                JOptionPane.showMessageDialog(null, "Search seccess!");
            }
        }

    }//GEN-LAST:event_btnSearchActionPerformed
