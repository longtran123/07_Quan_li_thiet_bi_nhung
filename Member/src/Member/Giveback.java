    private Connection con = null;
    private void loadTable(){
         try {
            con = Connect.connect();
            
            Statement s = con.createStatement();
            
            ResultSet rs = s.executeQuery("SELECT * FROM `giveback` WHERE FullName = '"+name+"'");
            String []colsName = {"ID", "Name","Project","Status","Quantity","Type"};
            tableModel.setColumnIdentifiers(colsName); 
            tblGiveback.setModel(tableModel);
            while(rs.next()){ 
                String Quan = String.valueOf(rs.getInt(6));
                Object rows[] = new Object[6];
                rows[0] = rs.getString(2);
                rows[1] = rs.getString(3);  
                rows[2] = rs.getString(4);
                rows[3] = rs.getString(5);
                rows[4] = Quan;
                rows[5] = rs.getString(7);
                tableModel.addRow(rows);
            }
            con.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
     private void Clear(){
        int dem = tblGiveback.getRowCount();
        try{
        while (dem > 0 ){
            tableModel.removeRow(0);
        }
        }catch(java.lang.ArrayIndexOutOfBoundsException e){
            e.getMessage();
        }
    }

