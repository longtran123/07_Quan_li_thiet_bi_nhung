     String EMAIL_REGEX = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        if(txtFN.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Full name not empty!");
        }else if(txtAdd.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Address not empty!");
        }else if(txtEM.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Email not empty!");
        }else if(!txtEM.getText().matches(EMAIL_REGEX)){
            JOptionPane.showMessageDialog(null, "Email not illegal!");
        }else {
             
               Change();
               JOptionPane.showMessageDialog(null, "Success!");
               setValue();
               this.dispose();
            }
         
       
        
    }//GEN-LAST:event_btnOKActionPerformed

