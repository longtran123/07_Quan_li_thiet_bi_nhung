        private String Day(){
        String DOB = null;
        if(cbbDay.getSelectedItem().equals("Day")){
            JOptionPane.showMessageDialog(null, "You need select the Day of birth");
        }else
        if(cbbMonth.getSelectedItem().equals("Month")){
            JOptionPane.showMessageDialog(null, "You need select the Month of birth");
        }else
        if(cbbYear.getSelectedItem().equals("Year")){
            JOptionPane.showMessageDialog(null, "You need select the Year of birth");
        }else {
        String Day = (String) cbbDay.getSelectedItem();
        String Month = (String) cbbMonth.getSelectedItem();
        String Year = (String) cbbYear.getSelectedItem();
        DOB = Year + "-" + Month + "-" + Day;
           
        }
        return DOB;
    }
        
    private void Change(){
        try {
           con = Connect.connect();
            Statement s = con.createStatement();
            String ID = lbID.getText();
            String FN = txtFN.getText();
            String Add = txtAdd.getText();
            String EM = txtEM.getText();
            String Gen = (String) cbbGen.getSelectedItem();
           
            String sql = "UPDATE `infomem` SET `FullName`='"+FN+"',`Address`='"+Add+"',`Email`='"+EM+"',`Gender`='"+Gen+"',`DOB`= '"+Day()+"' WHERE ID = '"+ID+"'";
            s.executeUpdate(sql);
            con.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
