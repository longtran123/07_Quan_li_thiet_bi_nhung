    private void setValue(){
            String FN = txtFN.getText();
            String Add = txtAdd.getText();
            String EM = txtEM.getText();
            String Gen = (String) cbbGen.getSelectedItem();
            String DOB = Day();
            InforAccount.tableModel.setValueAt(FN,row,3);
            InforAccount.tableModel.setValueAt(Add,row,4);
            InforAccount.tableModel.setValueAt(EM,row,5);
            InforAccount.tableModel.setValueAt(Gen,row,6);
            InforAccount.tableModel.setValueAt(DOB,row,7);
    }
    
